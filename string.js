function reverseMyString(str){
    let newString = ""
    for(let i = str.length - 1; i >= 0; i--){
        newString+=str[i];
    }
    return newString
}


console.log(reverseMyString('hello'))

console.log(reverseMyString('nagari'))


console.log(reverseMyString('salisu'))


console.log(reverseMyString('Aminu'))
 

 